        function configurarBusqueda() {
            const input = document.getElementById('busqueda-especialidad');
            const resultados = document.getElementById('search-results');
            const boton = document.getElementById('search-btn');
            if (!input || !resultados || !boton) return;

            function buscar() {
                const texto = input.value.trim().toLowerCase();
                const agrupadas = obtenerEspecialidadesAgrupadas();
                const coincidencias = Object.keys(agrupadas).filter(nombre => nombre.toLowerCase().includes(texto));

                if (!texto) {
                    resultados.style.display = 'none';
                    resultados.innerHTML = '';
                    return;
                }

                if (coincidencias.length === 0) {
                    resultados.innerHTML = '<div class="search-no-results">Sin resultados</div>';
                } else {
                    resultados.innerHTML = coincidencias.slice(0, 10).map(nombre => `<div class="search-result-item" data-especialidad="${nombre}"><strong>${nombre}</strong><br>${agrupadas[nombre].length} plantel(es)</div>`).join('');
                }
                resultados.style.display = 'block';
            }

            input.addEventListener('input', buscar);
            boton.addEventListener('click', buscar);
            resultados.addEventListener('click', function(e) {
                const item = e.target.closest('.search-result-item');
                if (!item) return;
                mostrarDetalleEspecialidad(item.dataset.especialidad);
                resultados.style.display = 'none';
                input.value = '';
            });
            document.addEventListener('click', function(e) {
                if (!e.target.closest('.search-container')) resultados.style.display = 'none';
            });
        }

        document.addEventListener('DOMContentLoaded', function() {
            renderPlanteles();
            configurarBusqueda();
        });
